using System; // using directive

namespace HelloWorld // Namespace declaration
{ // Start namespace block
    class Program // Create class
    { // Start class block
        static void Main(string[] args) // Create method
        { // Start method block
            Console
                .
                WriteLine
                (
                "Hello " +
                "World 1!"
                ); // Expression statement
            Console.WriteLine("Hello World 2!"); // Expression statement
            Console.WriteLine("Hello World 3!"); // Expression statement
            Console.WriteLine("Hello World 4!"); // Expression statement
            Console.WriteLine("Hello World 5!"); // Expression statement
            Console.WriteLine("Hello World 6!"); // Expression statement
        } // End method block
    } // End class block
} // End namespace block

