using System;

namespace Integers
{
    class Program
    {
        static void Main(string[] args)
        {
            Console.WriteLine("Hello World!");

            // Declaring and assign variable
            int counter = 5;

            // Output value to console
            Console.WriteLine("Counter is " + counter);

            // Change counter to 7
            counter = 7;

            // Output value to console
            Console.WriteLine("Counter is " + counter);

            // Increment counter by 1
            counter = counter + 1;

            // Output value to console
            Console.WriteLine("Counter is " + counter);

            // Increment counter by 1
            counter++;

            // Output value to console
            Console.WriteLine("Counter is " + counter);

            // Decrement counter by 1
            counter--;

            // Output value to console
            Console.WriteLine("Counter is " + counter);

            // Multiply by 3
            counter *= 3;

            // Output value to console
            Console.WriteLine("Counter is " + counter);

            // Divide by 3
            counter /= 3;

            // Output value to console
            Console.WriteLine("Counter is " + counter);

            // Modulus of 2
            counter %= 2;

            // Output value to console
            Console.WriteLine("Counter is " + counter);
        }
    }
}
